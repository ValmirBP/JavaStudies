package com.valmir.cursojava.aula13;

public class RelationalOperators {

	public static void main(String[] args) {
	
		int value1 = 1;
		int value2 = 2;
		
		System.out.println("value1 == value2 = " + (value1 == value2));
		System.out.println("value1 != value2 = " + (value1 != value2));
		System.out.println("value1 <= value2 = " + (value1 <= value2));
		System.out.println("value1 <  value2 = " + (value1 < value2));
		System.out.println("value1 >= value2 = " + (value1 >= value2));
		System.out.println("value1 >  value2 = " + (value1 > value2));
		
 
	}

}
